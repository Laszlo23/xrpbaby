#!/usr/bin/env node
/**
 * Print a fresh operator wallet + AGS distributor (stdout), or write to .secrets/ (gitignored).
 * One BIP39 mnemonic derives both accounts: m/44'/60'/0'/0/0 (operator) and .../1 (ags-distributor).
 * Do NOT commit private keys. Fund operator ~0.05 ETH on Base (8453) for Phase 0.
 *
 *   node scripts/generate-agent-wallets.mjs
 *   node scripts/generate-agent-wallets.mjs --write-secrets   # → b3/.secrets/agent-wallets.txt
 *   node scripts/generate-agent-wallets.mjs --encrypt   # requires `age` on PATH + AGE_RECIPIENT env
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { spawnSync } from "node:child_process";
import { toHex } from "viem";
import {
  english,
  generateMnemonic,
  mnemonicToAccount,
} from "viem/accounts";

const encrypt = process.argv.includes("--encrypt");
const writeSecrets = process.argv.includes("--write-secrets");

const mnemonic = generateMnemonic(english, 128);
const operator = mnemonicToAccount(mnemonic, { addressIndex: 0 });
const ags = mnemonicToAccount(mnemonic, { addressIndex: 1 });
const operatorPk = toHex(operator.getHdKey().privateKey);
const agsPk = toHex(ags.getHdKey().privateKey);

const body = [
  "Generated by scripts/generate-agent-wallets.mjs — keep offline; never commit.",
  "",
  "=== BIP39 mnemonic (back up once; anyone with this controls both addresses) ===",
  mnemonic,
  "",
  "=== Operator (Phase 0: fund ~0.05 ETH on Base 8453) ===",
  `path: m/44'/60'/0'/0/0`,
  `address: ${operator.address}`,
  `privateKey: ${operatorPk}`,
  "",
  "=== ags-distributor-1 (ECON_LIVE=1: fund mint price + gas; set AGENT_AGS_DISTRIBUTOR_PRIVATE_KEY) ===",
  `path: m/44'/60'/0'/0/1`,
  `address: ${ags.address}`,
  `privateKey: ${agsPk}`,
  "",
  "=== ENV snippet (server only) ===",
  `AGENT_AGS_DISTRIBUTOR_PRIVATE_KEY=${agsPk}`,
  "",
  "=== ops/agents.json ===",
  `Set "walletAddress" for ags-distributor-1 to: ${ags.address}`,
  "",
].join("\n");

if (writeSecrets) {
  const __dirname = path.dirname(fileURLToPath(import.meta.url));
  const b3Root = path.join(__dirname, "..");
  const dir = path.join(b3Root, ".secrets");
  const out = path.join(dir, "agent-wallets.txt");
  fs.mkdirSync(dir, { mode: 0o700 });
  fs.writeFileSync(out, body, { mode: 0o600 });
  console.log(`Wrote ${out} (chmod 600). Open it locally; do not commit or paste in chat.`);
} else {
  console.log(body);
}

if (encrypt) {
  const recipient = process.env.AGE_RECIPIENT?.trim();
  if (!recipient) {
    console.error("Set AGE_RECIPIENT (e.g. age1…) for --encrypt");
    process.exit(1);
  }
  const r1 = spawnSync("age", ["-e", "-r", recipient, "-o", "/dev/stdout"], {
    input: `${operatorPk}\n${agsPk}\n`,
    encoding: "utf-8",
  });
  if (r1.status !== 0) {
    console.error(r1.stderr || "age failed — install filippo.io/age");
    process.exit(1);
  }
  console.log("--- age ciphertext (store as /etc/bc-agents/wallets.age) ---");
  console.log(r1.stdout);
}
